# 11111111 -> 255

# 128 64 32 16 8 4 2 1

# 00001010 -> 10
# 00110101 -> 53
# 00001000 -> 8
# 00000101 -> 5

num01 = 8
num02 = 5

# 按位与
# 00001000 -> 8
# 00000101 -> 5
# 00000000 -> 0
print(num01 & num02)

# 按位或 |
# 00001000 -> 8
# 00000101 -> 5
# 00001101 -> 13
print(num01 | num02)

# 按位异或 ^
# 00001000 -> 8
# 00000101 -> 5
# 00001101 -> 13
print(num01 ^ num02)

# 按位取反（补码） -(x + 1)
# 00001000 -> 8
# 10001001 -> -9
print(~num01)

# 左移运算符
# 00001000 -> 8
# 00010000 -> 16
# 01000000 -> 64
# 100000000
print(num01 << 1)
print(num01 << 3)
print(num01 << 5)

# 右移运算符
# 00001000 -> 8
# 00000010 -> 2
print(num01 >> 2)